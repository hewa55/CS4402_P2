public class Assignment {
    private int var;
    private int val;

    public Assignment(int var, int val) {
        this.var = var;
        this.val = val;
    }

    public int getVar() {
        return var;
    }

    public void setVar(int var) {
        this.var = var;
    }

    public int getVal() {
        return val;
    }

    public void setVal(int val) {
        this.val = val;
    }
}
